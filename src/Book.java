
public class Book {
  private String name;
  private String edition;
  
  //constructor
  public Book(){
     name = "";
     edition = "";
  }

   public String getName() {
      return name;
   }

   public void setName(String name) {
      this.name = name;
   }

   public String getEdition() {
      return edition;
   }

   public void setEdition(String edition) {
      this.edition = edition;
   }
  
  
}
